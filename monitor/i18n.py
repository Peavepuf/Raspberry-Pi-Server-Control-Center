def normalize_language(language: str | None) -> str:
    raw = (language or "en").strip().lower()
    return raw if raw in SUPPORTED_LANGUAGES else "en"


def tr(language: str | None, key: str, **kwargs: object) -> str:
    lang = normalize_language(language)
    template = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["en"].get(key) or key
    return template.format(**kwargs) if kwargs else template
